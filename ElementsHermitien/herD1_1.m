    diag([6 6 repmat([12 6],1,n-2) 6],1) + ...
    diag([-6 -6 repmat([-12 -6],1,n-2) -6],-1) + ...
    diag(repmat([15 -2],1,n-1),2) + ...
    diag(repmat([-15 2],1,n-1),-2) + ...
    diag([repmat([-6 0],1,n-2) -6],3) + ...
    diag([repmat([6 0],1,n-2) 6],-3);

D1 = (1/30)*sparse(D1);